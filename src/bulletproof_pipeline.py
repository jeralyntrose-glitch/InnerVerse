def chunk_content(content: str, max_words: int = CHUNK_SIZE) -> List[str]:
    """Split content into small chunks to reduce blending opportunity"""
    words = content.split()
    chunks = []
    
    for i in range(0, len(words), max_words):
        chunk = ' '.join(words[i:i + max_words])
        if chunk.strip():
            chunks.append(chunk)
    
    return chunks


